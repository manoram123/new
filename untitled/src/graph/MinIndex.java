package graph;

public class MinIndex {

     // min index where the element is smallest in respective index...

    public static void main(String[] args) {
        int []arr = {8,9,3,5,7};
        for(int i=0; i<arr.length; i++){
            int minindex = -1;
            if(minindex == -1){
                minindex = i;
                System.out.println(minindex);
            }
        }

    }
}
